import UIKit

extension UIColor {
    static var backgroundPrimary: UIColor {
        return UIColor(named: "background_primary")!
    }
    
    static var backgroundSecondary: UIColor {
        return UIColor(named: "background_secondary")!
    }
    
    static var backgroundHighlight: UIColor {
        return UIColor(named: "background_highlight")!
    }
    
    static var textPrimary: UIColor {
        return UIColor(named: "text_primary")!
    }
}
